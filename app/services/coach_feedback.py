def build_tips(errors: List[str]) -> List[str]:
    """Build deduplicated coaching tips from error list."""
    tips: List[str] = []
    for err in errors:
        tip = ERROR_TIPS.get(err)
        if tip and tip not in tips:
            tips.append(tip)

    if not tips:
        tips.append("动作整体稳定，继续保持当前节奏。")

    return tips
